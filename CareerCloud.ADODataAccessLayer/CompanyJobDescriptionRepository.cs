            foreach (CompanyJobDescriptionPoco poco in items)
            {
                cmd.CommandText = @"INSERT INTO Company_Jobs_Descriptions (Id, Job, Job_Name, Job_Descriptions)
                                    VALUES (@Id, @Job, @Job_Name, @Job_Descriptions)";
